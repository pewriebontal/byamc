#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	to_find;

	to_find = (char)c;
	while (*s != to_find)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	return ((char *)s);
}
